   private void setTableFromList( ArrayList<Product> results){
        String col[] = {"ID","Name","Price","Image"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0){
          @Override
          public Class<?> getColumnClass(int column) {
            switch(column) {
                case 0: return Integer.class;
                case 1: return String.class;
                case 2: return Double.class;
                case 3: return ImageIcon.class;
                default: return Object.class;
            }
            }
        };
        
        jTable1.setRowHeight(80);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTable1.setModel(tableModel);
        
        for(int i=0;i<results.size();i++){
            Product p = results.get(i);
            int id = p.getId();
            String name = p.getName();
            double price = p.getPrice();
            BufferedImage picture = p.getPicture();
            Image dimg = picture.getScaledInstance(80, 80,Image.SCALE_SMOOTH);
            ImageIcon icon = new ImageIcon(dimg);
            
            Object[] row = {id, name,price,icon    };//กำหนดแถวให้มีค่า ตามแต่ละ attribute ของ Pokemon
            tableModel.addRow(row); //เพิ่มแถวเข้าไปในตาราง
        }
    }
