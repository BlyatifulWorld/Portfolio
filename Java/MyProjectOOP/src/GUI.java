    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      JFileChooser fileChooser = new JFileChooser();
        final int option = fileChooser.showOpenDialog(this);
            if (option != JFileChooser.APPROVE_OPTION) {
                return;
            }

            final File selectedFile = fileChooser.getSelectedFile();

            URL url;
            try {
                url = selectedFile.toURI().toURL();
            } catch (final MalformedURLException e1) {
                throw new RuntimeException(e1);
            }
            //jTextField4 : เก็บตำแหน่งไฟล์รูปที่อยู่ในเครื่อง
            jTextField4.setText(selectedFile.getAbsolutePath());
            try{
                BufferedImage img = ImageIO.read(new File(selectedFile.getAbsolutePath()));
                Image dimg = img.getScaledInstance(jLabel9.getWidth(), jLabel9.getHeight(),
                Image.SCALE_SMOOTH);
                final ImageIcon icon = new ImageIcon(dimg);
                //jLabel9 : ที่แสดงรูป
                jLabel9.setText("");
                jLabel9.setIcon(icon);
            }catch(Exception e){
                e.printStackTrace();
            }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int id = Integer.parseInt(jTextField1.getText());
        String name = jTextField2.getText();
        double price = Double.parseDouble(jTextField3.getText());
        //จัดการข้อมูลรูป
        BufferedImage picture =  null;
        try{
            //jTextField4 : กล่องข้อความที่เก็บตำแหน่งรูป
             picture = ImageIO.read(new File(jTextField4.getText()));
        }catch(Exception e){
             e.printStackTrace();
         }
        //จบจัดการข้อมูลรูป
        boolean success = product_service.insertProduct(id, name, price, picture);
        if(success){
            JOptionPane.showMessageDialog(null, "เพิ่มข้อมูลสำเร็จ");
        }else{
            JOptionPane.showMessageDialog(null, "มีข้อผิดพลาดในการเพิ่ม");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

